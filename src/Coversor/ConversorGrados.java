    private void btnCalculadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculadorActionPerformed
        // TODO add your handling code here:
        if(textCelcius.getText().length()>0){
            GraCelius = Double.parseDouble(textCelcius.getText());
            GraFarengei = (GraCelius* 1.8) + 32;
            GraKelvin = GraCelius + 237.15;
           this.textFarengei.setText(Double.toString(GraFarengei));
           this.textKelvin.setText(Double.toString(GraKelvin));          
            
        }else{
            JOptionPane.showMessageDialog(this, "Error, caja vacia...!");
        }
    }//GEN-LAST:event_btnCalculadorActionPerformed

